# Demanem la base i l'altura del triangle a l'usuari
base = float(input("Introdueix la base del triangle en cm: "))
altura = float(input("Introdueix l'altura del triangle en cm: "))

# Calculem l'àrea del triangle
area = (base * altura) / 2

# Mostrem l'àrea per pantalla
print(f"L'àrea del triangle és: {area} cm²")